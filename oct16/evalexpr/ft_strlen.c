void	ft_strlen(char *str)
{
	int i;
	char *str_buff;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	str_buff = (char *)malloc(sizeof(char) * i);
	ft_strcpy(str_buff, str);
}
