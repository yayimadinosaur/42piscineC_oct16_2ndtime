int		ft_iterative_factorial(int nb)
{
	int i;

	i = 1;
	while (nb > 0 && nb < 13)
	{
		i = i * nb;
		nb--;
	}
	if (nb > 12 || nb < 0)
	{
		return (0);
	}
	return (0);
}
