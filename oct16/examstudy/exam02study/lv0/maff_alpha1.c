#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(void)
{
	int i;
	int j;

	j = 97;
	i = 0;
	while (i < 26)
	{
		if (i % 2 == 0)
		{
			ft_putchar(j);
		}
		else
			ft_putchar(j - 32);
		j++;
		i++;
	}
	ft_putchar('\n');
}
